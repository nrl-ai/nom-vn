def push_spell(dry_run: bool) -> None:
    from huggingface_hub import HfApi, create_repo

    api = HfApi()
    if not dry_run:
        try:
            create_repo(SPELL_REPO_ID, repo_type="dataset", exist_ok=True)
        except Exception as exc:
            print(f"create_repo failed (continuing if exists): {exc}", flush=True)

    # Each register goes up under its own filename — load_dataset()
    # picks them via the configs YAML in the README.
    files: list[tuple[Path, str]] = []
    for _, fname, _ in SPELL_REGISTERS:
        src = SPELL_LOCAL / fname
        if not src.exists():
            raise SystemExit(f"missing source file {src}")
        files.append((src, fname))

    readme_path = REPO / "/tmp" / "spell_README.md"
    readme_path.parent.mkdir(parents=True, exist_ok=True)
    readme_path.write_text(SPELL_README, encoding="utf-8")
    files.append((readme_path, "README.md"))
    files.append((SPELL_LOCAL / "README.md", "README.vi.md"))

    print(f"  spell: {len(files)} files staged for {SPELL_REPO_ID}")
    for src, dst in files:
        print(f"    {src.relative_to(REPO) if src.is_relative_to(REPO) else src}  ->  {dst}")
    if dry_run:
        return

    for src, dst in files:
        api.upload_file(
            path_or_fileobj=str(src),
            path_in_repo=dst,
            repo_id=SPELL_REPO_ID,
            repo_type="dataset",
        )
        print(f"    pushed {dst}")


def push_ocr(dry_run: bool) -> None:
    """OCR ships in HF's `imagefolder` layout — one directory per
    condition (clean / noisy), with PNG files alongside a
    ``metadata.jsonl`` mapping ``file_name`` to ``text`` / ``id`` / ``font``.

    HF's dataset viewer auto-decodes the images via the ``Image()``
    feature when the metadata's ``file_name`` column points at sibling
    paths. The earlier base64-in-jsonl scheme broke the viewer
    ("size of the dataset is not coherent") because HF's viewer only
    recognises image columns when the bytes come from a real file
    reference, not an embedded blob.
    """
    from huggingface_hub import HfApi, create_repo

    api = HfApi()
    if not dry_run:
        try:
            create_repo(OCR_REPO_ID, repo_type="dataset", exist_ok=True)
        except Exception as exc:
            print(f"create_repo failed (continuing if exists): {exc}", flush=True)

    gt_path = OCR_LOCAL / "ground_truth.jsonl"
    rows = [json.loads(line) for line in gt_path.read_text().splitlines() if line.strip()]
    print(f"  ocr: {len(rows)} rows in ground truth")

    files: list[tuple[Path, str]] = []
    out_dir = Path("/tmp/ocr_publish")
    out_dir.mkdir(parents=True, exist_ok=True)

    for cond in ("clean", "noisy"):
        cond_dir = out_dir / cond
        cond_dir.mkdir(parents=True, exist_ok=True)
        meta_path = cond_dir / "metadata.jsonl"
        with meta_path.open("w", encoding="utf-8") as mf:
            for r in rows:
                src_png = OCR_LOCAL / r[cond]
                # Stage the PNG flat under the condition dir
                dst_png_name = f"{r['id']}.png"
                dst_png = cond_dir / dst_png_name
                dst_png.write_bytes(src_png.read_bytes())
                mf.write(
                    json.dumps(
                        {
                            "file_name": dst_png_name,
                            "id": r["id"],
                            "text": r["text"],
                            "font": r.get("font", ""),
                        },
                        ensure_ascii=False,
                    )
                    + "\n"
                )
                files.append((dst_png, f"{cond}/{dst_png_name}"))
        files.append((meta_path, f"{cond}/metadata.jsonl"))

    readme_path = Path("/tmp/ocr_README.md")
    readme_path.write_text(OCR_README, encoding="utf-8")
    files.append((readme_path, "README.md"))
    if (OCR_LOCAL / "README.md").exists():
        files.append((OCR_LOCAL / "README.md", "README.vi.md"))

    print(f"  ocr: {len(files)} files staged for {OCR_REPO_ID}")
    if dry_run:
        for _, dst in files[:6]:
            print(f"    {dst}")
        print(f"    ... ({len(files)} total)")
        return

    # Bulk upload via upload_folder for the per-condition dirs (~40 PNGs)
    for cond in ("clean", "noisy"):
        api.upload_folder(
            folder_path=str(out_dir / cond),
            path_in_repo=cond,
            repo_id=OCR_REPO_ID,
            repo_type="dataset",
        )
        print(f"    pushed {cond}/")
    api.upload_file(
        path_or_fileobj=str(readme_path),
        path_in_repo="README.md",
        repo_id=OCR_REPO_ID,
        repo_type="dataset",
    )
    print("    pushed README.md")
    if (OCR_LOCAL / "README.md").exists():
        api.upload_file(
            path_or_fileobj=str(OCR_LOCAL / "README.md"),
            path_in_repo="README.vi.md",
            repo_id=OCR_REPO_ID,
            repo_type="dataset",
        )
        print("    pushed README.vi.md")


def main() -> None:
    p = argparse.ArgumentParser(description=__doc__, formatter_class=argparse.RawTextHelpFormatter)
    p.add_argument("--datasets", nargs="+", choices=["spell", "ocr"], default=["spell", "ocr"])
    p.add_argument("--dry-run", action="store_true", help="Stage files but don't push.")
    args = p.parse_args()

    if "spell" in args.datasets:
        print(f"=== {SPELL_REPO_ID} ===")
        push_spell(args.dry_run)
    if "ocr" in args.datasets:
        print(f"=== {OCR_REPO_ID} ===")
        push_ocr(args.dry_run)

    if args.dry_run:
        print("\nDRY RUN — nothing pushed. Re-run without --dry-run to publish.")
    else:
        print("\nPushed. Verify with:")
        print("  hf api datasets/nrl-ai/vn-spell-correction-eval-real")
        print(
            "  python -c 'from datasets import load_dataset;"
            ' ds = load_dataset("nrl-ai/vn-spell-correction-eval-real", "forum");'
            " print(ds)'"
        )
        print("Open the HF page and confirm there's no yellow YAML warning banner.")


if __name__ == "__main__":
    sys.exit(main())
